        public boolean validarFormulario(String cantidad) { // VALIDACION DE TXT MONTO
        boolean next = false;
        Pattern patGastos = Pattern.compile("^[0-9]+([.])?([0-9]+)?$");
        Matcher matGastos = patGastos.matcher(cantidad);

        if (matGastos.matches()&&!cantidad.equals("")) {
            next = true;

        } else {
            JOptionPane.showMessageDialog(null, "Solo Numeros");
            
        }
        return next;
    }
    
